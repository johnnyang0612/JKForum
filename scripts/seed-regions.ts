async function main() {
  let total = 0;
  let cityOrder = 0;
  for (const [city, districts] of Object.entries(REGIONS)) {
    let districtOrder = 0;
    for (const district of districts) {
      await db.region.upsert({
        where: { city_district: { city, district } },
        create: { city, district, sortOrder: cityOrder * 1000 + districtOrder },
        update: { sortOrder: cityOrder * 1000 + districtOrder },
      });
      total++;
      districtOrder++;
    }
    cityOrder++;
  }
  console.log(`OK ${total} 個區域 seed 完成（${Object.keys(REGIONS).length} 縣市）`);
}

main().then(() => db.$disconnect());
